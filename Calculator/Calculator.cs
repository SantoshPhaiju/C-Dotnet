using System;

namespace OOP.Calculator;

public class Calculator
{
    public double Add(double a, double b)
    {
        return a + b;
    }

    public double Divide(int a, int b)
    {
        return a / b;
    }

    public int Mul(int a, int b)
    {
        return a * b;
    }

    public int Sub(int a, int b)
    {
        return a - b;
    }
}
